    protected void adicionarModulosPermissao() {
        // Lista M처dulos Recursivamente >>>
        this.daoModulo.setConnection("sal");
        this.daoModulo.open();
        try {
            this.criarPermissoes(null);
        } catch (SQLException ex) {
            Msg.erro(this, "Erro ao gerar permiss천es!!!\n\n" + ex.getMessage());
        }
        this.daoModulo.close();
        // Lista M처dulos Recursivamente >>>
    }

    private void criarPermissoes(Integer idModuloPai) throws SQLException {
        this.daoModulo.consultarModulo(this.idFuncionario, idModuloPai);

        ResultSet RS = this.daoModulo.getRS();

        while (RS.next()) {
            ModuloBean modulo = new ModuloBean();
            modulo.setDescricao(RS.getString("descricao"));
            modulo.setIdModulo(RS.getInt("idModulo"));

            JCheckBoxPermissao check = new JCheckBoxPermissao();
            check.setText(RS.getString("descricao"));
            check.setModulo(modulo);

            if (idModuloPai == null) { // Raiz
                check.setBackground(Color.LIGHT_GRAY);
            }

            if (RS.getBoolean("temAcesso")) { // Menu Raiz
                check.setSelected(true);
            }

            this.pnAddPerm.add(check);

            if (RS.getInt("Itens") > 0) { // Tem Itens Filhos
                this.criarPermissoes(RS.getInt("idModulo"));
            }
        }
    }
